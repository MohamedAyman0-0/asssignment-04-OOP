public class Rectangle
{
    public int Width { get; }
    public int Height { get; }

   
    public Rectangle()
    {
        Width = 0;
        Height = 0;
    }

   
    public Rectangle(int width, int height)
    {
        Width = width;
        Height = height;
    }

    
    public Rectangle(int side)
    {
        Width = side;
        Height = side;
    }
}
